const weekdays = ['mon','tue','wed']

// push() - add item to array - at the end
weekdays.push('thur')
console.log(weekdays);

//unshift() - add item to array - at the beginning
weekdays.unshift('sun')
console.log(weekdays);

weekdays.push(7)
console.log(weekdays);

//pop() - removes element from the end
weekdays.pop()
console.log(weekdays);

//shift() - remove items from the beginning
weekdays.shift()
console.log(weekdays);
